    private void mostrarCitas() {
       
         mdlCitas.setNumRows(0);
        SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
        
        for(Cita cita:citadao.listar()){
            Object[] fila={cita.getCodigo(), cita.getPaciente(), sdf.format(cita.getFechaAtencion()),
                cita.getDoctor(), cita.getEspecialidad()};
            mdlCitas.addRow(fila);
            
        }
    }



