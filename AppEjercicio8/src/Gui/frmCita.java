    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // OBTENER FECHA en TXTField
        SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
        
        try {
            java.util.Date fecha=sdf.parse(txtFechaAtencion.getText());
            java.sql.Date fechaSQL= new java.sql.Date(fecha.getTime());
            
            String paciente=txtPaciente.getText();
            String doctor=txtPaciente.getText();
            String especialidad=cboEspecialidad.getSelectedItem().toString();
            
            Cita cita=new Cita();
            cita.setPaciente(paciente);
            cita.setFechaAtencion(fechaSQL);
            cita.setDoctor(doctor);
            cita.setEspecialidad(especialidad);
            
            citadao.guardar(cita);
            mostrarCitas();
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

